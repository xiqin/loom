#!/usr/bin/env node

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const SCHEMA_PATH = join(ROOT, 'config', 'pipeline.schema.json');

const AUTO_GEN_TAG = '<!-- AUTO-GENERATED by scripts/generate-pipeline-docs.mjs — DO NOT EDIT -->';

function writeIfChanged(filePath, content) {
  const existing = existsSync(filePath) ? readFileSync(filePath, 'utf-8') : '';
  if (existing === content) {
    console.log(`  · ${filePath} — already up to date`);
    return false;
  }
  writeFileSync(filePath, content, 'utf-8');
  console.log(`  ✔ ${filePath}`);
  return true;
}

function loadSchema() {
  const schema = JSON.parse(readFileSync(SCHEMA_PATH, 'utf-8'));
  return {
    states: schema.states,
    totalSteps: schema.progressFileFormat.progressBar.totalSteps,
    statusValues: schema.progressFileFormat.statusValues,
    transitions: schema.transitions,
  };
}

const SHORT_NAMES = {
  'loom-brainstorming': 'brainstorming',
  'loom-writing-plans': 'writing-plans',
  'loom-using-git-worktrees': 'git-worktree',
  'loom-subagent-driven-development': 'subagent-dev',
  'loom-verification-before-completion': 'verification',
  'loom-index-update': 'index-update',
};

function shortName(skillId) {
  return SHORT_NAMES[skillId] || skillId.replace('loom-', '');
}

function generatePipelineMd(schema) {
  const { states, totalSteps, transitions } = schema;  const pipelineSteps = Object.entries(states)
    .filter(([, s]) => s.step != null && typeof s.step === 'number' && s.skill)
    .sort(([, a], [, b]) => a.step - b.step);

  const allStates = Object.entries(states);

  let md = `${AUTO_GEN_TAG}\n\n`;
  md += `# 流水线定义（core/pipeline.md）\n\n`;
  md += `> Schema 定义：[\`config/pipeline.schema.json\`](../config/pipeline.schema.json)\n\n`;

  md += `## 功能级状态机\n\n`;
  md += `每个功能（\`specs/<date+feature>/\`）独立跟踪以下 ${allStates.length} 个状态：\n\n`;
  md += `| 状态 | 对应 Step | 说明 |\n`;
  md += `|------|----------|------|\n`;
  for (const [name, state] of allStates) {
    const stepLabel = state.step == null ? '—' : `Step ${state.step}`;
    md += `| \`${name}\` | ${stepLabel} | ${state.description} |\n`;
  }

  md += `\n\`\`\`mermaid\nstateDiagram-v2\n`;
  md += `    [*] --> brainstorming\n`;
  for (const t of transitions) {
    md += `    ${t.from} --> ${t.to} : ${t.description}\n`;
  }
  md += `\`\`\`\n\n`;

  md += `## ${totalSteps} 步流水线\n\n`;
  md += `loom 框架的核心是 ${totalSteps} 步流水线，定义了从需求到代码交付的完整执行流程。\n\n`;

  md += `\`\`\`\n`;
  md += pipelineSteps.map(([, s]) => shortName(s.skill)).join(' → ');
  md += `\n\`\`\`\n\n`;

  const letters = 'ABCDEFGHIJ';
  md += `\`\`\`mermaid\ngraph LR\n`;
  for (let i = 0; i < pipelineSteps.length; i++) {
    const label = shortName(pipelineSteps[i][1].skill);
    if (i < pipelineSteps.length - 1) {
      const nextLabel = shortName(pipelineSteps[i + 1][1].skill);
      md += `    ${letters[i]}[${label}] --> ${letters[i + 1]}[${nextLabel}]\n`;
    }
  }
  md += `\`\`\`\n\n`;

  md += `### 流水线阶段\n\n`;
  md += `| Step | 阶段 | 说明 | 输出 |\n`;
  md += `|------|------|------|------|\n`;
  const OUTPUT_MAP = {
    'loom-brainstorming': '`specs/<date+feature>/spec.md`',
    'loom-writing-plans': '`specs/<date+feature>/plan.md`',
    'loom-using-git-worktrees': 'feature 分支',
    'loom-subagent-driven-development': '源码 + 测试报告',
    'loom-verification-before-completion': '验证报告',
    'loom-index-update': 'ENGINEERING-INDEX.md',
  };
  for (const [, state] of pipelineSteps) {
    const output = OUTPUT_MAP[state.skill] || '—';
    md += `| ${state.step} | ${shortName(state.skill)} | ${state.description} | ${output} |\n`;
  }

  md += `\n### 阶段串联规则\n\n`;
  md += `\`\`\`\n`;
  for (let i = 0; i < pipelineSteps.length; i++) {
    const name = shortName(pipelineSteps[i][1].skill);
    const nextName = i < pipelineSteps.length - 1 ? shortName(pipelineSteps[i + 1][1].skill) : null;
    if (i === 0 || i === 1) {
      md += `${name} 完成 → 等待用户确认 → ${nextName}\n`;
    } else if (name === 'verification') {
      md += `${name} 通过 → 触发 ${nextName}\n`;
      md += `${name} 未通过 → 修复后重新验证\n`;
    } else if (nextName) {
      md += `${name} 完成 → 触发 ${nextName}\n`;
    } else {
      md += `${name} 完成 → 通知可以提交\n`;
    }
  }
  md += `\`\`\`\n\n`;
  md += `**严令禁止跳过任何步骤。每个步骤完成后必须显式触发下一步。**\n\n`;

  md += `### 状态横幅规范\n\n`;
  md += `每个阶段开始和结束时必须输出状态横幅，格式：\n\n`;
  md += `\`\`\`\n`;
  md += `━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n`;
  md += ` pipeline [<进度条>] Step N/${totalSteps} — <阶段名称> (<skill名>)\n`;
  md += ` skill:   <skill名>\n`;
  md += ` 功能:    <功能名>\n`;
  md += ` status:  ▶ 开始执行 | ✅ 完成 | ❌ 失败\n`;
  md += ` 下一步:  → Step N+1: <下一阶段>\n`;
  md += `━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n`;
  md += `\`\`\`\n\n`;
  md += `进度条规则：${totalSteps} 格，已完成的步骤填 ■，未到达的填 □。当前步骤通过 Step N/${totalSteps} 标识，状态通过 status 行显示（▶ 进行中 / ✅ 完成 / ❌ 失败）。\n\n`;

  md += `### 前置条件检查\n\n`;
  md += `每个阶段开始前必须检查前置条件：\n\n`;
  md += `| 阶段 | 前置条件 |\n`;
  md += `|------|---------|\n`;
  for (const [, state] of pipelineSteps) {
    const preconditions = state.preconditions ? state.preconditions.map(p => p.description).join(' + ') : '—';
    md += `| ${shortName(state.skill)} | ${preconditions} |\n`;
  }
  md += `\n前置条件未满足时，必须停止并提示用户。\n`;

  return md;
}

function generateProgressTrackerMd(schema) {
  const { totalSteps, statusValues } = schema;
  const pipelineSteps = Object.entries(schema.states)    .filter(([, s]) => s.step != null && typeof s.step === 'number' && s.skill)
    .sort(([, a], [, b]) => a.step - b.step);

  let md = `${AUTO_GEN_TAG}\n\n`;
  md += `# 进度追踪规范（core/progress-tracker.md）\n\n`;
  md += `> Schema 定义：[\`config/pipeline.schema.json\`](../config/pipeline.schema.json) → \`progressFileFormat\`\n\n`;

  md += `## progress.md\n\n`;
  md += `每个功能开发过程中，维护 \`specs/<date+feature>/progress.md\`，可视化流水线状态。\n\n`;

  md += `\`\`\`mermaid\nstateDiagram-v2\n`;
  md += `    [*] --> 等待\n`;
  md += `    等待 --> 进行中\n`;
  md += `    进行中 --> 完成\n`;
  md += `    进行中 --> 失败\n`;
  md += `    失败 --> 进行中\n`;
  md += `    完成 --> [*]\n`;
  md += `\`\`\`\n\n`;

  md += `### 文件格式\n\n`;
  md += `\`\`\`markdown\n`;
  md += `# <功能名> — 开发流水线\n\n`;
  md += `**开始时间：** YYYY-MM-DD HH:mm\n`;
  md += `**当前阶段：** Step N/${totalSteps}\n\n`;

  md += `| Step | 阶段 | 状态 | 开始时间 | 完成时间 | 备注 |\n`;
  md += `|------|------|------|---------|---------|------|\n`;
  for (const [, state] of pipelineSteps) {
    const label = shortName(state.skill);
    let status, time, completeTime, note;
    if (state.step <= 2) {
      status = '✅ 完成'; time = 'HH:mm'; completeTime = 'HH:mm'; note = '';
    } else if (state.step === 3) {
      status = '✅ 完成'; time = 'HH:mm'; completeTime = 'HH:mm'; note = '分支: feature/xxx';
    } else if (state.step === 4) {
      status = '▶ 进行中'; time = 'HH:mm'; completeTime = '—'; note = 'task 3/8';
    } else {
      status = '⏳ 等待'; time = '—'; completeTime = '—'; note = '';
    }
    md += `| ${state.step} | ${label} | ${status} | ${time} | ${completeTime} | ${note} |\n`;
  }

  md += `\n## Skill 调用记录\n\n`;
  md += `| 时间 | Skill | 触发原因 | 结果 |\n`;
  md += `|------|-------|---------|------|\n`;
  md += `| HH:mm | brainstorming | 用户提出需求 | ✅ 已完成 |\n`;
  md += `| HH:mm | writing-plans | 设计确认，开始拆 plan | ✅ 已完成 |\n`;
  md += `| HH:mm | subagent-dev | plan 确认，开始编码 | ▶ 执行中 |\n`;
  md += `\`\`\`\n\n`;

  md += `### 状态值\n\n`;
  md += `| 状态 | 含义 |\n`;
  md += `|------|------|\n`;
  for (const [, val] of Object.entries(statusValues)) {
    md += `| ${val.symbol} ${val.label} | ${val.description} |\n`;
  }

  md += `\n### 更新规则\n\n`;
  md += `- 每个 skill 开始执行时：更新对应行状态为 \`▶ 进行中\`，填写开始时间\n`;
  md += `- 每个 skill 执行结束时：更新状态为 \`✅ 完成\`，填写完成时间\n`;
  md += `- 在 Skill 调用记录中追加一行\n`;
  md += `- 如果某步骤失败：状态改为 \`❌ 失败\`，备注写失败原因\n\n`;

  md += `### 进度条（状态横幅中使用）\n\n`;
  md += `\`\`\`\n`;
  const filled = '■';
  const empty = '□';
  for (let i = 4; i <= totalSteps; i++) {
    const bar = filled.repeat(i) + empty.repeat(totalSteps - i);
    const suffix = i === 4 ? ' — 进行中' : i === totalSteps ? ' — 完成' : ' — 验证中';
    md += `[${bar}] Step ${i}/${totalSteps}${suffix}\n`;
  }
  md += `\`\`\`\n`;

  return md;
}

const schema = loadSchema();

console.log('Pipeline docs generation:\n');
writeIfChanged(join(ROOT, 'core', 'pipeline.md'), generatePipelineMd(schema));
writeIfChanged(join(ROOT, 'core', 'progress-tracker.md'), generateProgressTrackerMd(schema));
console.log('\n✔ Pipeline docs generation complete');
